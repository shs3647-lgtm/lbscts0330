function textWidth(text) {
  if (!text) return 0;
  let w = 0;
  for (const ch of text) {
    const code = ch.charCodeAt(0);
    if (code >= 0xAC00 && code <= 0xD7AF) w += 2;
    else if (code >= 0x3000 && code <= 0x9FFF) w += 2;
    else w += 1.1;
  }
  return w;
}

function calcAutoWidths(headers, rows, minWidths) {
  return headers.map((h, i) => {
    const headerW = textWidth(h) + 2;
    let maxDataW = 0;
    for (const row of rows) {
      const cellW = textWidth(row[i] || '');
      if (cellW > maxDataW) maxDataW = cellW;
    }
    const min = minWidths?.[i] ?? 8;
    return Math.min(Math.max(headerW, maxDataW + 3, min), 60);
  });
}

function isLongColumn(width) { return width > 16; }

