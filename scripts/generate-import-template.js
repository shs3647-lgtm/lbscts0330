/**
 * FC 고장사슬 병합셀 (v4.0 — 13열 N:1:N)
 * 병합 대상: 공정번호(col 4), FM(col 5) — FM 그룹 단위
 */
function applyFCSheetMergeCells(worksheet, rows) {
  if (rows.length < 2) return;
  // FM 그룹 감지: col 4(공정번호)가 비어있지 않으면 새 그룹
  const groups = [];
  let groupStart = 0;
  for (let i = 1; i <= rows.length; i++) {
    const isNewGroup = i === rows.length || (rows[i]?.[3] && rows[i][3].toString().trim() !== '');
    if (isNewGroup && i > groupStart) {
      groups.push({ start: groupStart, end: i - 1 });
      groupStart = i;
    }
  }
  // 각 그룹에 대해 공정번호(col 4)와 FM(col 5) 병합
  for (const { start, end } of groups) {
    if (end > start) {
      const startRow = start + 2; // +2: 헤더(1) + 0-based(1)
      const endRow = end + 2;
      try {
        worksheet.mergeCells(startRow, 4, endRow, 4); // 공정번호
        worksheet.mergeCells(startRow, 5, endRow, 5); // FM
        const c4 = worksheet.getCell(startRow, 4);
        const c5 = worksheet.getCell(startRow, 5);
        c4.alignment = { ...c4.alignment, vertical: 'middle', horizontal: 'center' };
        c5.alignment = { ...c5.alignment, vertical: 'middle', horizontal: 'center' };
      } catch (e) {
        console.warn(`[병합 경고] rows ${startRow}-${endRow}:`, e.message);
      }
    }
  }
}

