/** 워크북 생성 헬퍼 */
function buildWorkbook(sheetDefs, sampleData, mode) {
  const wb = new ExcelJS.Workbook();
  wb.creator = `FMEA Smart System ${TEMPLATE_VERSION}`;
  wb.created = new Date();

  const emptyWidths = {
    'L2-1.공정번호': 14, 'L2-2.공정명': 20, '공정유형코드(선택)': 16,
    'L2-3.공정기능(설명)': 40, 'L2-4.제품특성': 22, '특별특성': 10, 'L2-5.고장형태': 25,
    '4M': 8, 'L3-1.작업요소(설비)': 28, '★작업요소(B1)': 18, 'L3-2.요소기능': 35, 'L3-3.공정특성': 22, 'L3-4.고장원인': 30,
    'L1-1.구분': 14, 'L1-2.제품(반)기능': 40, 'L1-3.제품(반)요구사항': 25, 'L1-4.고장영향': 30,
    'FE구분': 10, 'FE(고장영향)': 40, 'S': 5, 'FM(고장형태)': 25, 'WE(작업요소)': 20, 'FC(고장원인)': 30,
    'B5.예방관리': 20, 'A6.검출관리': 20, 'O': 5, 'D': 5, 'AP': 5,
    'DC추천1': 25, 'DC추천2': 25, 'PC추천1': 25, 'PC추천2': 25, 'O추천': 8, 'D추천': 8,
  };

  sheetDefs.forEach(def => {
    const ws = wb.addWorksheet(def.name, { properties: { tabColor: { argb: def.color } } });
    const rows = sampleData ? (sampleData[def.name] || []) : [];
    const minWidths = def.headers.map((h) => h === '4M' || h === 'S' || h === 'O' || h === 'D' || h === 'AP' ? 5 : 12);

    if (mode === 'sample' && rows.length > 0) {
      const colWidths = calcAutoWidths(def.headers, rows, minWidths);
      ws.columns = def.headers.map((header, i) => ({ header, key: `col${i}`, width: colWidths[i] }));
      const hr = ws.getRow(1); hr.height = 24; hr.eachCell(c => applyHeaderStyle(c, def.color));
      rows.forEach((data, idx) => {
        const row = ws.addRow(data);
        const bg = idx % 2 === 0 ? 'FFFFFF' : 'F0F7FB';
        row.eachCell((cell, cn) => applyDataCellStyle(cell, colWidths[cn - 1] ?? 20, bg));
      });
      if (def.name === 'FC 고장사슬' && rows.length > 1) applyFCSheetMergeCells(ws, rows);
    } else {
      ws.columns = def.headers.map((header) => ({ header, key: header, width: emptyWidths[header] ?? 20 }));
      const hr = ws.getRow(1); hr.height = 24; hr.eachCell(c => applyHeaderStyle(c, def.color));
      for (let i = 0; i < 10; i++) {
        const row = ws.addRow(def.headers.map(() => ''));
        row.eachCell(c => applyDataStyle(c));
      }
    }
    ws.views = [{ state: 'frozen', xSplit: 1, ySplit: 1 }];
  });

  return wb;
}

