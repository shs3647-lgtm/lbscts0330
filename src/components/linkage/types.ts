/**
 * 연동 그룹 번호 추출
 */
export function extractLinkGroupNo(docId: string): number {
    const match = docId.match(/-L(\d{2})$/i);
    return match ? parseInt(match[1]) : 0;
}

/**
 * 연동 상태 확인
 */
export function isLinked(docId: string): boolean {
    return /-L\d{2}$/i.test(docId);
}

/**
 * ID를 연동 상태로 변환
 */
export function setLinkedId(docId: string, linkGroupNo: number = 1): string {
    const base = docId.replace(/-(L\d{2}|S)$/i, '');
    return `${base}-L${String(linkGroupNo).padStart(2, '0')}`;
}

/**
 * ID를 단독 상태로 변환
 */
export function setSoloId(docId: string): string {
    const base = docId.replace(/-(L\d{2}|S)$/i, '');
    return `${base}-S`;
}

/**
 * 다음 연동 그룹 번호 계산
 */
export function getNextLinkGroupNo(docs: LinkedDocItem[]): number {
    const linkedDocs = docs.filter(d => d.linkGroupNo > 0);
    if (linkedDocs.length === 0) return 1;
    const maxNo = Math.max(...linkedDocs.map(d => d.linkGroupNo));
    return maxNo + 1;
}
