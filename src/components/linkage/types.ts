export type SourceModule = 'apqp' | 'pfm' | 'dfm' | 'pfd' | 'cp';
export type TargetModule = 'pfm' | 'pfd' | 'cp';
export type DocType = 'M' | 'F' | 'P' | 'D' | 'T' | 'L' | 'S';  // D: DFMEA, P: PFD, T: Trial, L: Launch, S: Service

/**
 * 연동 문서 아이템
 */
export interface LinkedDocItem {
    id: string;             // 예: pfd26-m001-L01
    module: TargetModule;   // pfd, cp
    docType: DocType;       // M, F, P (또는 CP: T, L, P, S)
    subject?: string;       // 제목 (동기화 시 자동)
    linkGroupNo: number;    // 0=Solo, 1~99=Linked
    isAutoGenerated: boolean; // 자동생성 여부 (L01)
    status: 'linked' | 'solo' | 'pending';
}

/**
 * 소스 문서 정보 (등록정보 공유용)
 */
export interface SourceDocInfo {
    id: string;
    module: SourceModule;
    subject: string;
    customerName?: string;
    modelYear?: string;
    companyName?: string;
    docType: DocType;
}

